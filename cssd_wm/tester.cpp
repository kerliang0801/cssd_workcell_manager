#include "mysql_connection.h"

#include <cppconn/driver.h>
#include <cppconn/exception.h>
#include <cppconn/resultset.h>
#include <cppconn/statement.h>

using namespace std;

int main(void)
{


try {
  sql::Driver *driver;
  sql::Connection *con;
  sql::Statement *stmt;
  sql::ResultSet *res;

  /* Create a connection */
  driver = get_driver_instance();
  con = driver->connect("tcp://127.0.0.1:3306", "malcomneo", "malcomneo");
  /* Connect to the MySQL test database */
  con->setSchema("inventory");

  stmt = con->createStatement();
  res = stmt->executeQuery("SELECT item, COUNT(*) FROM quantity GROUP BY item");
  res->last();
  int last_row = res->getRow();
  res->first();

  while ( int(res->getRow()) <= last_row ) 
        {
            

            cout<<res->getString("item")<<" "<< res->getString("count(*)")<<endl;
            res->next();	

        }
  delete res;
  delete stmt;
  delete con;

} catch (sql::SQLException &e) {
  cout << "# ERR: SQLException in " << __FILE__;
  cout << "(" << __FUNCTION__ << ") on line "      << __LINE__ << endl;
  cout << "# ERR: " << e.what();
  cout << " (MySQL error code: " << e.getErrorCode();
  cout << ", SQLState: " << e.getSQLState() << " )" << endl;
}

cout << endl;

return EXIT_SUCCESS;
}